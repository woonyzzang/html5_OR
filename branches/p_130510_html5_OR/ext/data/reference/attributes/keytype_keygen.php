	<section>
		<h2 class="s_tit">keytype 속성 (keygen)</h2>

		<dl class="definition">
		<dt>정의</dt>
		<dd>keytype 속성은 키 유형(보안 알고리즘)을 정의합니다.</dd>
		</dl>

		<h3 class="s_tit">속성 값</h3>
		<p>나열 속성이며, 키워드와 상태를 나열합니다. 키워드가 &#34;RSA&#34;인 경우 상태는 RSA가 됩니다. 유효하지 않은 키워드의 상태 값은 &#34;unknown&#34;입니다. 사용자 에이전트는 자신이 지원하는 알고리즘에 해당하는 키워드를 유효한 것으로 인식합니다. 속성을 지원한다면 기본값은 RSA이고, 지원하지 않는다면 &#34;unknown&#34; 상태가 됩니다.</p>

		<h2 class="s_tit">disabled 속성 (command)</h2>

		<dl class="definition">
		<dt>정의</dt>
		<dd>disabled 속성은 참과 거짓의 불리언 속성입니다. 따라서 이 속성이 존재한다면 커맨드가 현재 상태에서 사용 불가능함을 나타냅니다.</dd>
		</dl>

		<h3 class="s_tit">속성 값</h3>
		<p>disabled 속성은 disabled 속성 그 자체를 선언하거나 또는 disabled=“disabled” 로 선언하면 참, 속성이 선언되지 않을 경우 거짓으로 구분됩니다.</p>

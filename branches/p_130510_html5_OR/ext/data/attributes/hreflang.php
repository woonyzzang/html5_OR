		</dl>

		<h3 class="s_tit">속성 값</h3>
		<p>속성에 지정할 코드는 IETF BCP47 &#34;Matching of Language Tags&#34;를 따르며, 지금까지 사용한 언어 코드와 같으므로 특별히 신경 쓸 점은 없습니다.</p>

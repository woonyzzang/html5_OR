		<h2 class="s_tit">reversed 속성 (ol)</h2>

		<dl class="definition">
		<dt>정의</dt>
		<dd>reversed 속성을 사용하면 리스트의 순서가 역순(…, 3, 2, 1)이 됩니다. 숫자를 역순으로 표현하는 것이지 리스트의 순서를 뒤부터 표현하는 것은 아닙니다. 속성을 사용하지 않으면 정순(1, 2, 3, …)으로 표시합니다.</dd>
		</dl>

		<h3 class="s_tit">속성 값</h3>
		<p>불리언 속성입니다.</p>
